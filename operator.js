// 연산자 (Operator)

//연산자는 하나 이상의 표현식을 대상으로 산술, 할당, 비교, 논리, 타입, 지수 연산 등을 수행해 하나의 값을 만든다.
//이때 연산의 대상을 피연산자라 한다.

// 산술 연산자
  5 * 4 // -> 20

// 문자열 연결 연산자
  'My name is' + 'Lee' //  -> 'My name is Lee'

// 할당 연산자
  var Color = 'red';

// 비교 연산자
  3 > 5 // -> false;

// 논리 연산자
  true && false // -> false;

// 타입 연산자
  typeof 'hi' // -> String

// 피연산자가 값의 역할을 한다면 연산자는 피연산자를 연산하여 새로운 값을 만든다 라는 동사의 역할을 한다고 볼 수 있다.

